const CACHE_NAME = "pwa-cache-v28";   // <--- hochzÃ¤hlen bei Ã„nderungen

const URLS_TO_CACHE = [
  "./",
  "./index.html",
  "./manifest.json",
  "./assets/iuk-192.png",
  "./assets/iuk-512.png",
  "./assets/iuk-lernwelt-512.png",
  "./assets/avatar-default.png"   // Default-Avatar
];

// Installation: wichtige App-Dateien vorab cachen
self.addEventListener("install", (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => cache.addAll(URLS_TO_CACHE))
  );
});

// Alte Caches lÃ¶schen
self.addEventListener("activate", (event) => {
  event.waitUntil(
    caches.keys().then((cacheNames) =>
      Promise.all(
        cacheNames
          .filter((name) => name !== CACHE_NAME)
          .map((name) => caches.delete(name))
      )
    )
  );
});

// Fetch-Handler
self.addEventListener("fetch", (event) => {
  const url = new URL(event.request.url);

  // ðŸ‘‰ Nur GET-Anfragen behandeln
  if (event.request.method !== "GET") {
    event.respondWith(fetch(event.request));
    return;
  }

  // ðŸ‘‰ Firebase Storage: NIE cachen (Profilbilder etc.)
  if (url.origin.includes("firebasestorage.googleapis.com")) {
    event.respondWith(fetch(event.request));
    return;
  }

  // ðŸ‘‰ Externe Ressourcen (CDNs, APIs): immer Netz
  if (url.origin !== self.location.origin) {
    event.respondWith(fetch(event.request));
    return;
  }

  // ðŸ‘‰ Eigene Assets (z. B. /iuk-app/assets/...): Cache mit Fallback Netz
  if (url.pathname.includes("assets/")) {
    event.respondWith(
      caches.match(event.request).then((cachedResponse) => {
        return cachedResponse || fetch(event.request);
      })
    );
    return;
  }

  // ðŸ‘‰ Standard: Cache-First fÃ¼r App-Dateien
  event.respondWith(
    caches.match(event.request).then((cachedResponse) => {
      if (cachedResponse) {
        return cachedResponse;
      }
      return fetch(event.request).then((networkResponse) => {
        return caches.open(CACHE_NAME).then((cache) => {
          cache.put(event.request, networkResponse.clone());
          return networkResponse;
        });
      });
    })
  );
});
